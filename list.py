# Membuat list
fruits = ['Apel', 'jeruk', 'cherry']

# Mengakses item
print(fruits[0])  # Output: Apel

# Menambah item
fruits.append('orange')

# Menghapus item
fruits.remove('jeruk')

# Mengurutkan list
fruits.sort()

# Menampilkan list akhir
print(fruits)  # Output: ['Apel', 'cherry', 'jeruk']